package com.SpringRestAssignment_6;


public class CalculatorService {

	
	public int addService(int a,int b) {
		return a+b;
	}
	
    public int subtractService(int a,int b) {
		return a-b;
	}

    public int multiplyService(int a,int b) {
		return a*b;
	}
    
    public int divideService(int a,int b) {
		return a/b;
	}
    
}
